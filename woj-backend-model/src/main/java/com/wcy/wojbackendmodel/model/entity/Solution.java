package com.wcy.wojbackendmodel.model.entity;

import com.baomidou.mybatisplus.annotation.*;
import lombok.Data;

import java.io.Serializable;
import java.util.Date;

/**
 * 题目题解
 * @TableName solution
 */
@TableName(value ="solution")
@Data
public class Solution implements Serializable {
    /**
     * id
     */
    @TableId(type = IdType.AUTO)
    private Long id;

    /**
     * 标题
     */
    private String title;

    /**
     * 内容
     */
    private String content;

    /**
     * 标签列表（json 数组）
     */
    @TableField(value = "tags",updateStrategy = FieldStrategy.IGNORED)
    private String tags;

    /**
     * 特殊标签列表（json 数组）
     */
    @TableField(value = "specialTags",updateStrategy = FieldStrategy.IGNORED)
    private String specialTags;

    /**
     * 题目 id
     */
    private Long questionId;

    /**
     * 创建用户 id
     */
    private Long userId;

    /**
     * 创建时间
     */
    private Date createTime;

    /**
     * 更新时间
     */
    private Date updateTime;

    /**
     * 是否删除
     */
    private Integer isDelete;

    @TableField(exist = false)
    private static final long serialVersionUID = 1L;
}